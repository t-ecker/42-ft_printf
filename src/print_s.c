#include "../ft_printf.h"

int	ft_putstr(char *c)
{
	int	i;

	i = 0;
	if (!c)
		return (ft_putstr("(null)"));
	while (c[i])
		i++;
	check(write(1, c, i), 0);
	return (i);
}
